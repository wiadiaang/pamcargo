                        <h3>Choosing the best tools from the very beginning</h3>
                        <p>Choose the right shape</p>
                        <ul>
                            <li>Choosing the wrong type of packaging could leave your contents at risk but, if you think about what you're sending, you'll know the right equipment.</li>
                            <li>Put powders and fine grains in strong plastic bags, seal them securely and then pack the bag in a quality cardboard box.</li>
                            <li>Use triangular tubes not cylinders to pack rolled plans, maps and blueprints.</li>
                            <li>Package all other items in strong cardboard boxes which will keep your bits and pieces safe and secure.</li>
                        </ul>
                        <p>Remember that size matters</p>
                        <ul>
                            <li>Under-filled boxes are likely to collapse as they travel along machines with other parcels, and overloaded ones could burst. Your contents should fit snugly inside the box with 5 cm of padding.</li>
                        </ul>
                        <p>Quality counts</p>
                        <ul>
                            <li>Consider strength, cushioning, and durability when you choose your packaging. Choose boxes made of corrugated cardboard, with good quality outer liners, and, if your contents are a bit more fragile, use heavy-duty, double-layered boxes</li>
                        </ul>
                    </div>
                    <div id="packed" class="tab-pane fade">
                        <h3>Perfectly packing the contents</h3>
                        <p>Wrap them up</p>
                        <ul>
                            <li>As you parcel travels far and wide, it's unavoidably subject to shocks. Stop your contents damaging each other by wrapping each items separately in bubble wrap and keeping your fragile items right in the centre of the parcel.</li>
                        </ul>
                        <p>Cushion from the bottom and sides</p>
                        <ul>
                            <li>Pop some padding in the bottom of your box followed by a layer of double corrugated card before you put your wrapped items in. Then cushion the contents with at least 5cm of padding all around.</li>
                        </ul>
                        <p>Cushion from the top</p>
                        <ul>
                            <li>Finally, pop some more double corrugated card on top of the padding to make sure everything stays in place. It's best to leave some room to place extra cushioning on top of this too.</li>
                        </ul>
                    </div>
                    <div id="touches" class="tab-pane fade">
                        <h3>Ensure the finishing touches to your parcel</h3>
                        <p>The seal of approval</p>
                        <ul>
                            <li>Close the flaps on your box carefully so that none of your padding escapes. Then seal your parcel with 5cm wide, strong parcel tape to stop it flying open.</li>
                        </ul>
                        <p>Warn the couriers</p>
                        <ul>
                            <li>If your parcel is particularly heavy (25kg or over), give the courier a heads up by marking it as "Heavy". It will save their back and minimise the risk of them dropping it in surprise.</li>
                        </ul>
                        <p>Keep it simple</p>
                        <ul>
                            <li>Don't use strings or cords: they can get tangled up in machines. Just pop your address, shipping labels and customs information on the top surface of the box and avoid putting it across the parcel tape as there may be movement here.</li>
                        </ul>
